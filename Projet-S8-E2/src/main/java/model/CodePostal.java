/**
 * La classe CodePostal est conçue pour gérer les codes postaux de chaque commune
 * @author imane
 * @version 1.0
 */
package model;

public class CodePostal {
	/** le code postal d'une commune */
	private int codePostal;
	/**
	 * getter du code postal d'une commune
	 * @return le code postal d'une commune
	 */
	public int getCodePostal() {
		return codePostal;
	}
	/**
	 * setter du code postal d'une commune
	 * @param codePostal
	 */
	public void setCodePostal(int codePostal) {
		this.codePostal = codePostal;
	}
	

}
